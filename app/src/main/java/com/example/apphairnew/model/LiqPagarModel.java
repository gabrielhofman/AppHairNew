package com.example.apphairnew.model;

public class LiqPagarModel {

    private String liqPagarVencimento;
    private Float liqPagarValor;
    private String liqPagarObservacao;

    public String getLiqPagarVencimento() {
        return liqPagarVencimento;
    }

    public void setLiqPagarVencimento(String liqPagarVencimento) {
        this.liqPagarVencimento = liqPagarVencimento;
    }

    public Float getLiqPagarValor() {
        return liqPagarValor;
    }

    public void setLiqPagarValor(Float liqPagarValor) {
        this.liqPagarValor = liqPagarValor;
    }

    public String getLiqPagarObservacao() {
        return liqPagarObservacao;
    }

    public void setLiqPagarObservacao(String liqPagarObservacao) {
        this.liqPagarObservacao = liqPagarObservacao;
    }
}
