package com.example.apphairnew.model;

public class FluxoModel {


    private int idFluxo;
    private double valorFluxo;
    private String movFluxo;
    private int agendaFluxo;
    private String dataFluxo;
    private int usuarioFluxo;
    private int idAgenda;


    public int getIdFluxo() {
        return idFluxo;
    }

    public void setIdFluxo(int idFluxo) {
        this.idFluxo = idFluxo;
    }

    public double getValorFluxo() {
        return valorFluxo;
    }

    public void setValorFluxo(double valorFluxo) {
        this.valorFluxo = valorFluxo;
    }

    public String getMovFluxo() {
        return movFluxo;
    }

    public void setMovFluxo(String movFluxo) {
        this.movFluxo = movFluxo;
    }

    public int getAgendaFluxo() {
        return agendaFluxo;
    }

    public void setAgendaFluxo(int agendaFluxo) {
        this.agendaFluxo = agendaFluxo;
    }

    public String getDataFluxo() {
        return dataFluxo;
    }

    public void setDataFluxo(String dataFluxo) {
        this.dataFluxo = dataFluxo;
    }

    public int getUsuarioFluxo() {
        return usuarioFluxo;
    }

    public void setUsuarioFluxo(int usuarioFluxo) {
        this.usuarioFluxo = usuarioFluxo;
    }

    public int getIdAgenda() {
        return idAgenda;
    }

    public void setIdAgenda(int idAgenda) {
        this.idAgenda = idAgenda;
    }
}
